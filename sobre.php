<h2>SOBRE NÓS</h2>
            
<p>O site Ambiente Parintins foi desenvolvido pelos alunos Marcos de Souza Tavares e Bartolomeu Lima, orientado pelo professor David Brito Ramos, é o resultado final de um projeto desenvolvido para defesa de conclusão do curso Técnico em Informática no IFAM Parintins, no ano de 2021. O objetivo do site é conscientizar, informar, sensibilizar e mostrar soluções sustentáveis, junto com outras medidas, para amenizar os danos que são causados no meio ambiente, principalmente, da cidade de Parintins no estado do Amazonas.</p>

